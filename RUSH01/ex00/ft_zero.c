char	ft_zero(void)
{
	char	cuad[4][4];
	int		a;
	int		b;

	a = 0;
	while (a < 4)
	{
		b = 0;
		while (b < 4)
		{
			if (cuad[a][b] == 0)
				cuad[a][b] = '0';
			b++;
		}
		a++;
	}
	return (cuad[a][b]);
}
