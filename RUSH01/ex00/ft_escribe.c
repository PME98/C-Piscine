#include<unistd.h>

void	ft_putchar(char c);
void	ft_escribe(char c [4][4])
{
	int	a;
	int	b;


	a = 0;
	while (a < 4)
	{	
		b = 0;
		while (b < 4)
		{
		ft_putchar(c[a][b]);
		b++;
		}	
		write (1, "\n", 1);
		a++;
	}
}
